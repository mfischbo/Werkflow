package de.artignition.werkflow.plugin;

public abstract class PluginExitStatus {

	public enum Status {
		SUCCESS,
		IDLE,
		FAILED
	}
	
	protected Status exitStatus;

	public Status getExitStatus() {
		return exitStatus;
	}

	public void setExitStatus(Status exitStatus) {
		this.exitStatus = exitStatus;
	}
}
